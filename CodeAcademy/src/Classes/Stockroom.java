package Classes;

public class Stockroom {
    int StockroomID;
    String Location;
    String Address;
    String StockroomName;
}
    public String setStockroomID(int StockroomID) {
        StockroomID = setStockroomID();
    }

    public String setLocation(String Location) {
        Location = setLocation();
    }

    public String setAddress(String Address) {
        Address = setAddress();
    }

    public String setStockroomName(String StockroomName) {
        StockroomName = setStockroomName();
    }
